const os = require('os');

// It return OS type
console.log(os.type());

// It return OS Platform
console.log(os.platform());

// It return architecture
console.log(os.arch());

// It return Host name
console.log(os.hostname());

// It return OS release Data
console.log(os.release());

//It return OS uptime
console.log(os.uptime());

//It return user Info
console.log(os.userInfo());

// It give total memory
console.log(os.totalmem());

//It give free memory
console.log(os.freemem());

//It return cpu information
console.log(os.cpus());